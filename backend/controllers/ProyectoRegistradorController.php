<?php

namespace backend\controllers;

use Yii;
use yii\filters\AccessControl;
use common\models\ProyectoRegistrador;
use common\models\ProyectoRegistradorSearch;
use frontend\models\UnidadEjecutora;
use yii\web\Controller;
use yii\web\NotFoundHttpException;
use yii\filters\VerbFilter;
use \yii\web\Response;
use yii\helpers\Html;

/**
 * ProyectoRegistradorController implements the CRUD actions for ProyectoRegistrador model.
 */
class ProyectoRegistradorController extends \common\controllers\BaseController
{
    public function behaviors()
    {
        return parent::behaviors();
    }

    /**
     * Lists all ProyectoRegistrador models.
     * @return mixed
     */
    public function actionIndex()
    {
        $searchModel = new ProyectoRegistradorSearch();
        $dataProvider = $searchModel->search(Yii::$app->request->queryParams);

        return $this->render('index', [
            'searchModel' => $searchModel,
            'dataProvider' => $dataProvider,
        ]);
    }

    /**
     * Displays a single ProyectoRegistrador model.
     * @param integer $id
     * @return mixed
     */
    public function actionView($id)
    {
        return $this->render('view', [
            'model' => $this->findModel($id),
        ]);
    }

    /**
     * Creates a new ProyectoRegistrador model.
     * For ajax request will return json object
     * and for non-ajax request if creation is successful, the browser will be redirected to the 'view' page.
     * @return mixed
     */
    public function actionCreate($proyecto)
    {
        $request = Yii::$app->request;
        $model = new ProyectoRegistrador();
        $model->id_proyecto = $proyecto;
        $unidadEjecutora = UnidadEjecutora::find()->all();

        if($request->isAjax){
            /*
            *   Process for ajax request
            */
            Yii::$app->response->format = Response::FORMAT_JSON;
            if($request->isGet){
                return [
                    'title'=> "Create new ProyectoRegistrador",
                    'content'=>$this->renderAjax('create', [
                        'model' => $model,
                        'unidadEjecutora' => $unidadEjecutora,
                    ]),
                    'footer'=> Html::button('Close',['class'=>'btn btn-default pull-left','data-dismiss'=>"modal"]).
                                Html::button('Save',['class'=>'btn btn-primary','type'=>"submit"])
        
                ];         
            }else if($model->load($request->post()) && $model->save()){
                return [
                    'forceReload'=>'#responsable',
                    'title'=> "Create new ProyectoRegistrador",
                    'content'=>'<span class="text-success">Create ProyectoRegistrador success</span>',
                    'footer'=> Html::button('Close',['class'=>'btn btn-default pull-left','data-dismiss'=>"modal"])
        
                ];         
            }else{           
                return [
                    'title'=> "Create new ProyectoRegistrador",
                    'content'=>$this->renderAjax('create', [
                        'model' => $model,
                        'unidadEjecutora' => $unidadEjecutora,
                    ]),
                    'footer'=> Html::button('Close',['class'=>'btn btn-default pull-left','data-dismiss'=>"modal"]).
                                Html::button('Save',['class'=>'btn btn-primary','type'=>"submit"])
        
                ];         
            }
        }else{
            /*
            *   Process for non-ajax request
            */
            if ($model->load($request->post()) && $model->save()) {
                return $this->redirect(['proyecto/view', 'id' => $model->id_proyecto]);
            } else {
                return $this->render('create', [
                    'model' => $model,
                    'unidadEjecutora' => $unidadEjecutora,
                ]);
            }
        }
       
    }

    /**
     * Updates an existing ProyectoRegistrador model.
     * For ajax request will return json object
     * and for non-ajax request if update is successful, the browser will be redirected to the 'view' page.
     * @param integer $id
     * @return mixed
     */
    public function actionUpdate($id)
    {
        $request = Yii::$app->request;
        $model = $this->findModel($id);  
        $unidadEjecutora = UnidadEjecutora::find()->all();     

        if($request->isAjax){
            /*
            *   Process for ajax request
            */
            Yii::$app->response->format = Response::FORMAT_JSON;
            if($request->isGet){
                return [
                    'title'=> "Update ProyectoRegistrador #".$id,
                    'content'=>$this->renderAjax('update', [
                        'model' => $model,
                        'unidadEjecutora' => $unidadEjecutora,
                    ]),
                    'footer'=> Html::button('Close',['class'=>'btn btn-default pull-left','data-dismiss'=>"modal"]).
                                Html::button('Save',['class'=>'btn btn-primary','type'=>"submit"])
                ];         
            }else if($model->load($request->post()) && $model->save()){
                return [
                    'forceReload'=>'#responsable',
                    'title'=> "ProyectoRegistrador #".$id,
                    'content'=>$this->renderAjax('view', [
                        'model' => $model,
                        'unidadEjecutora' => $unidadEjecutora,
                    ]),
                    'footer'=> Html::button('Close',['class'=>'btn btn-default pull-left','data-dismiss'=>"modal"]).
                            Html::a('Edit',['update','id'=>$id],['class'=>'btn btn-primary','role'=>'modal-remote'])
                ];    
            }else{
                 return [
                    'title'=> "Update ProyectoRegistrador #".$id,
                    'content'=>$this->renderAjax('update', [
                        'model' => $model,
                        'unidadEjecutora' => $unidadEjecutora,
                    ]),
                    'footer'=> Html::button('Close',['class'=>'btn btn-default pull-left','data-dismiss'=>"modal"]).
                                Html::button('Save',['class'=>'btn btn-primary','type'=>"submit"])
                ];        
            }
        }else{
            /*
            *   Process for non-ajax request
            */
            if ($model->load($request->post()) && $model->save()) {
                return $this->redirect(['proyecto/view', 'id' => $model->id_proyecto]);
            } else {
                return $this->render('update', [
                    'model' => $model,
                    'unidadEjecutora' => $unidadEjecutora,
                ]);
            }
        }
    }

    /**
     * Deletes an existing ProyectoRegistrador model.
     * If deletion is successful, the browser will be redirected to the 'index' page.
     * @param integer $id
     * @return mixed
     */
    public function actionDelete($id)
    {
        $request = Yii::$app->request;
        $model = $this->findModel($id);
        $proyecto = $model->id_proyecto;
        $model->delete();

        if($request->isAjax){
            /*
            *   Process for ajax request
            */
            Yii::$app->response->format = Response::FORMAT_JSON;
            return ['forceClose'=>true,'forceReload'=>'#registrador'];
        }else{
            /*
            *   Process for non-ajax request
            */
            return $this->redirect(['proyecto/view', 'id' => $proyecto]);
        }
    }

    /**
     * Finds the ProyectoRegistrador model based on its primary key value.
     * If the model is not found, a 404 HTTP exception will be thrown.
     * @param integer $id
     * @return ProyectoRegistrador the loaded model
     * @throws NotFoundHttpException if the model cannot be found
     */
    protected function findModel($id)
    {
        if (($model = ProyectoRegistrador::findOne($id)) !== null) {
            return $model;
        } else {
            throw new NotFoundHttpException('The requested page does not exist.');
        }
    }
}
